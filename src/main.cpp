// эта функция resolve_from_config решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этой строкой фиксируем промежуточный результат
std::string resolve_from_config(const std::string& config_path, const std::string& p) {
// проверяем условие чтобы отсеять неподходящий сценарий
    if (p.empty()) return p;
// эта функция pp решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const std::filesystem::path pp(p);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (pp.is_absolute()) return pp.string();
// эта функция absolute решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const std::filesystem::path cfg = std::filesystem::absolute(std::filesystem::path(config_path));
// эта функция parent_path решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const std::filesystem::path base = cfg.parent_path();
// возвращаем рассчитанный результат в вызывающий код
    return (base / pp).lexically_normal().string();
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция is_fixture_or_test_path решает отдельную задачу внутри общего потока работы
