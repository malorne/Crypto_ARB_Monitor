// делаем небольшой шаг и готовим почву для следующих строк
        "curl -sS -L -w '\\n__HTTP_STATUS__:%{http_code}' '" + shell_escape_single_quotes(url) + "' 2>/dev/null";
// эта функция pipe решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
