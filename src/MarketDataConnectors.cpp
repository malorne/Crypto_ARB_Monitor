    PipeReader pipe(cmd);

// делаем небольшой шаг и готовим почву для следующих строк
    std::string out;
// делаем небольшой шаг и готовим почву для следующих строк
    char buf[4096];
// крутим цикл пока условие остается истинным
    while (true) {
// эта функция fread решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
        const size_t n = std::fread(buf, 1, sizeof(buf), pipe.get());
// проверяем условие чтобы отсеять неподходящий сценарий
        if (n > 0) out.append(buf, n);
// проверяем условие чтобы отсеять неподходящий сценарий
        if (n < sizeof(buf)) break;
// закрываем текущий блок и поднимаемся уровнем выше
    }
// эта функция close решает отдельную задачу внутри общего потока работы
