    std::string t = str::trim_copy(token);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (t.size() < 2 || t.front() != '"' || t.back() != '"') return t;
// эта функция substr решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
    t = t.substr(1, t.size() - 2);
// делаем небольшой шаг и готовим почву для следующих строк
    std::string out;
// эта функция reserve решает отдельную задачу внутри общего потока работы
