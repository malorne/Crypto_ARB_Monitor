// эта функция ConfigManager решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// тут закрываем небольшой кусок общей задачи
ConfigManager::ConfigManager(std::string path) : path_(std::move(path)) {}

// эта функция load решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// тут делаем очередной рабочий шаг и двигаемся дальше
void ConfigManager::load() {
    // этой строкой подсказываем смысл соседнего блока
// эта функция in решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    std::ifstream in(path_);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!in) throw ConfigError("Cannot open: " + path_);

// эта функция clear решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    kv_.clear();
// делаем небольшой шаг и готовим почву для следующих строк
    std::string line;
// крутим цикл пока условие остается истинным
    while (std::getline(in, line)) {
        // тут коротко поясняем зачем нужен этот кусок
// эта функция trim_copy решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
        line = str::trim_copy(line);
// проверяем условие чтобы отсеять неподходящий сценарий
        if (line.empty() || line[0] == '#') continue;

// эта функция find решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
        auto pos = line.find('=');
// проверяем условие чтобы отсеять неподходящий сценарий
        if (pos == std::string::npos) continue;

// эта функция trim_copy решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
        std::string key = str::trim_copy(line.substr(0, pos));
// эта функция trim_copy решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
        std::string val = str::trim_copy(line.substr(pos + 1));
// сохраняем значение в переменную чтобы использовать дальше
        kv_[key] = val;
// закрываем текущий блок и поднимаемся уровнем выше
    }
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция get_string решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// здесь проводим маленький но важный технический шаг
std::optional<std::string> ConfigManager::get_string(const std::string& key) const {
// эта функция find решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
    auto it = kv_.find(key);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (it == kv_.end()) return std::nullopt;
// возвращаем рассчитанный результат в вызывающий код
    return it->second;
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция get_double решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этот кусок нужен чтобы код шел по понятному сценарию
std::optional<double> ConfigManager::get_double(const std::string& key) const {
// эта функция get_string решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
    auto s = get_string(key);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!s) return std::nullopt;
// в этом блоке аккуратно ловим возможные исключения
    try {
// возвращаем рассчитанный результат в вызывающий код
        return std::stod(*s);
// этой строкой фиксируем промежуточный результат
    } catch (...) {
        // здесь оставляем понятную подсказку по текущему шагу
// бросаем исключение чтобы явно подсветить ошибочный кейс
        throw ConfigError("Bad double for key: " + key);
// закрываем текущий блок и поднимаемся уровнем выше
    }
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция get_bool решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// здесь проводим маленький но важный технический шаг
std::optional<bool> ConfigManager::get_bool(const std::string& key) const {
// эта функция get_string решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// сохраняем значение в переменную чтобы использовать дальше
    auto s = get_string(key);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!s) return std::nullopt;

// проверяем условие чтобы отсеять неподходящий сценарий
    if (*s == "true" || *s == "1") return true;
// проверяем условие чтобы отсеять неподходящий сценарий
    if (*s == "false" || *s == "0") return false;
// бросаем исключение чтобы явно подсветить ошибочный кейс
    throw ConfigError("Bad bool for key: " + key);
// закрываем текущий блок и поднимаемся уровнем выше
}

