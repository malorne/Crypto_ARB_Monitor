// тут просто продолжаем собирать итоговый результат
std::string make_temp_config_path(const std::string& test_name) {
// эта функция now решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const auto stamp = std::chrono::steady_clock::now().time_since_epoch().count();
// эта функция temp_directory_path решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const auto path = std::filesystem::temp_directory_path() /
// эта функция path решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
                      std::filesystem::path("am_cfg_" + test_name + "_" + std::to_string(stamp) + ".conf");
// возвращаем рассчитанный результат в вызывающий код
    return path.string();
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция write_temp_config решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этой строкой фиксируем промежуточный результат
std::string write_temp_config(const std::string& test_name, const std::string& body) {
// эта функция make_temp_config_path решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const std::string path = make_temp_config_path(test_name);
// эта функция out решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    std::ofstream out(path, std::ios::binary);
// доклеиваем следующую часть данных в выходной поток
    out << body;
// эта функция close решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    out.close();
// возвращаем рассчитанный результат в вызывающий код
    return path;
// закрываем текущий блок и поднимаемся уровнем выше
}

