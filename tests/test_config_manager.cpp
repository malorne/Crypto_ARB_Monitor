// Пояснение к текущему шагу.
// make_temp_config_path: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Продолжение общей логики.
std::string make_temp_config_path(const std::string& test_name) {
// now: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Фиксируем ожидаемое значение.
    const auto stamp = std::chrono::steady_clock::now().time_since_epoch().count();
// temp_directory_path: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Фиксируем ожидаемое значение.
    const auto path = std::filesystem::temp_directory_path() /
// path: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
                      std::filesystem::path("am_cfg_" + test_name + "_" + std::to_string(stamp) + ".conf");
// Возвращаем вычисленное значение.
    return path.string();
// Конец текущего блока.
}

// write_temp_config: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// этой строкой фиксируем промежуточный результат
std::string write_temp_config(const std::string& test_name, const std::string& body) {
// make_temp_config_path: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Фиксируем ожидаемое значение.
    const std::string path = make_temp_config_path(test_name);
// out: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    std::ofstream out(path, std::ios::binary);
// доклеиваем следующую часть данных в выходной поток
    out << body;
// close: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    out.close();
// Возвращаем вычисленное значение.
    return path;
// Конец текущего блока.
}

