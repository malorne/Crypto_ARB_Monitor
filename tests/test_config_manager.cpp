// Путь данных: вход -> проверки -> результат.
// Проверяем отдельный сценарий.
TEST(ConfigManager, LoadsAndTrimsValues) {
    // Пояснение к алгоритму.
// write_temp_config: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Фиксируем ожидаемое значение.
    const std::string path = write_temp_config(
// тут закрываем небольшой кусок общей задачи
        "loads_and_trims",
// Рабочий шаг в общей цепочке.
        "# comment\n"
// Значение сохраняется для следующих шагов.
        " name =   monitor  \n"
// Значение сохраняется для следующих шагов.
        " fee_bps =  12.5 \n"
// Значение сохраняется для следующих шагов.
        " enabled = true\n");

// cfg: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    am::ConfigManager cfg(path);
// load: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    cfg.load();

// ASSERT_EQ: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    ASSERT_EQ(cfg.get_string("name").has_value(), true);
// EXPECT_EQ: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    EXPECT_EQ(*cfg.get_string("name"), "monitor");
// ASSERT_EQ: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    ASSERT_EQ(cfg.get_double("fee_bps").has_value(), true);
// EXPECT_NEAR: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    EXPECT_NEAR(*cfg.get_double("fee_bps"), 12.5, 1e-12);
// ASSERT_EQ: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    ASSERT_EQ(cfg.get_bool("enabled").has_value(), true);
// EXPECT_EQ: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    EXPECT_EQ(*cfg.get_bool("enabled"), true);

// remove: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    std::filesystem::remove(path);
// Конец текущего блока.
}

